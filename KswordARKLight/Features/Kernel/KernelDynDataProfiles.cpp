DynDataProfileMatch FindMatchingDynDataProfile(const ksword::ark::ArkDynModuleIdentity& identity) {
    DynDataProfileMatch result;
    std::vector<std::wstring> diagnostics;

    if (!identity.present) {
        result.message = L"当前 ntoskrnl identity 不可用，无法匹配本地 DynData profile pack。";
        return result;
    }

    std::uint32_t existingPackCount = 0;
    std::uint32_t scannedProfileCount = 0;
    for (const std::filesystem::path& candidatePath : ProfilePackSearchPaths()) {
        std::error_code ec;
        if (!std::filesystem::exists(candidatePath, ec) || !std::filesystem::is_regular_file(candidatePath, ec)) {
            AppendDiagnostic(diagnostics, L"pack 不存在: " + candidatePath.wstring());
            continue;
        }

        result.scanned = true;
        existingPackCount += 1U;
        result.existingPackCount = existingPackCount;
        result.path = candidatePath.wstring();

        std::string jsonText;
        std::wstring readError;
        if (!ReadFileBytes(candidatePath, jsonText, readError)) {
            AppendDiagnostic(diagnostics, readError + L": " + candidatePath.wstring());
            continue;
        }

        JsonValue rootObject;
        std::string parseError;
        JsonParser parser(jsonText);
        if (!parser.parse(rootObject, parseError) || !rootObject.isObject()) {
            AppendDiagnostic(diagnostics, L"pack JSON 解析失败: " + candidatePath.wstring() + L" (" + Utf8ToWideLocal(parseError) + L")");
            continue;
        }

        std::uint32_t schemaVersion = 0;
        std::uint32_t packVersion = 0;
        if (!ParseUInt32(rootObject.member("schemaVersion"), schemaVersion)) {
            schemaVersion = 1U;
        }
        if (!ParseUInt32(rootObject.member("packVersion"), packVersion) ||
            schemaVersion != 1U ||
            (packVersion != 1U && packVersion != 2U && packVersion != 3U)) {
            AppendDiagnostic(diagnostics, L"pack schemaVersion/packVersion 不支持: " + candidatePath.wstring());
            continue;
        }

        const JsonValue* profiles = rootObject.member("profiles");
        if (profiles == nullptr || !profiles->isArray() || profiles->arrayValue.empty()) {
            AppendDiagnostic(diagnostics, L"pack profile 列表为空: " + candidatePath.wstring());
            continue;
        }
        const std::uint32_t profileCount = static_cast<std::uint32_t>(std::min<std::size_t>(profiles->arrayValue.size(), std::numeric_limits<std::uint32_t>::max()));
        result.profileCount = profileCount;

        for (const JsonValue& profileObject : profiles->arrayValue) {
            if (!profileObject.isObject()) {
                continue;
            }
            scannedProfileCount += 1U;
            result.scannedProfileCount = scannedProfileCount;
            if (!ProfileMatchesIdentity(identity, profileObject)) {
                continue;
            }
            return BuildMatchedProfileResult(identity, rootObject, profileObject, candidatePath, existingPackCount, profileCount, scannedProfileCount);
        }
        AppendDiagnostic(diagnostics, L"pack 未命中: " + candidatePath.wstring() + L" (profiles=" + std::to_wstring(profileCount) + L")");
    }

    result.message = L"未找到匹配 DynData profile pack；存在 pack=" + std::to_wstring(existingPackCount) +
        L"，扫描 profile=" + std::to_wstring(scannedProfileCount) + L"。";
    const std::wstring detail = JoinWide(diagnostics, L" | ");
    if (!detail.empty()) {
        result.message += L" " + detail;
    }
    result.existingPackCount = existingPackCount;
    result.scannedProfileCount = scannedProfileCount;
    return result;
}

void AppendDynDataProfileRows(KernelOperationResult& result, const DynDataProfileMatch& match, const std::wstring& filterText) {
    PushFilteredRow(result, Row({
        { L"Source", L"Local DynData Profile Pack" },
        { L"Scanned", match.scanned ? L"是" : L"否" },
        { L"Matched", match.matched ? L"是" : L"否" },
        { L"Valid", match.valid ? L"是" : L"否" },
        { L"ApplyMode", ApplyModeText(match) },
        { L"Profile", Utf8ToWideLocal(match.profile.profileName) },
        { L"PdbName", Utf8ToWideLocal(match.profile.pdbName) },
        { L"PdbGuid", Utf8ToWideLocal(match.profile.pdbGuid) },
        { L"PdbAge", std::to_wstring(match.profile.pdbAge) },
        { L"Coverage", CoverageText(match.coveragePercent) },
        { L"Fields", std::to_wstring(match.profile.fields.size()) + L"/" + std::to_wstring(match.fieldCount) },
        { L"Items", std::to_wstring(match.profileEx.items.size()) + L"/" + std::to_wstring(match.typedItemCount) },
        { L"CallbackItems", std::to_wstring(match.callbackItemCount) },
        { L"PackProfiles", std::to_wstring(match.profileCount) },
        { L"ScannedProfiles", std::to_wstring(match.scannedProfileCount) },
        { L"ExistingPacks", std::to_wstring(match.existingPackCount) },
        { L"ProfilePath", match.path },
    }, match.message), filterText);

    PushFilteredRow(result, Row({
        { L"Source", L"Current ntoskrnl Identity" },
        { L"Module", match.profile.ntoskrnl.moduleName },
        { L"Class", std::to_wstring(match.profile.ntoskrnl.classId) },
        { L"Machine", HexTextLocal(match.profile.ntoskrnl.machine) },
        { L"TimeDateStamp", HexTextLocal(match.profile.ntoskrnl.timeDateStamp) },
        { L"SizeOfImage", HexTextLocal(match.profile.ntoskrnl.sizeOfImage) },
        { L"ImageBase", HexTextLocal(match.profile.ntoskrnl.imageBase) },
    }), filterText);

    const std::size_t fieldLimit = std::min<std::size_t>(match.profile.fields.size(), 32U);
    for (std::size_t index = 0; index < fieldLimit; ++index) {
        const ksword::ark::DynDataProfileField& field = match.profile.fields[index];
        PushFilteredRow(result, Row({
            { L"Source", L"Profile Legacy Field" },
            { L"Index", std::to_wstring(index) },
            { L"FieldId", std::to_wstring(field.fieldId) },
            { L"Offset", HexTextLocal(field.offset) },
            { L"ApplyMode", L"Legacy" },
            { L"Profile", Utf8ToWideLocal(match.profile.profileName) },
        }), filterText);
    }
    if (match.profile.fields.size() > fieldLimit) {
        PushFilteredRow(result, Row({
            { L"Source", L"Profile Legacy Field" },
            { L"Status", L"Truncated" },
            { L"Shown", std::to_wstring(fieldLimit) },
            { L"Total", std::to_wstring(match.profile.fields.size()) },
        }), filterText);
    }

    const std::size_t itemLimit = std::min<std::size_t>(match.profileEx.items.size(), 48U);
    for (std::size_t index = 0; index < itemLimit; ++index) {
        const ksword::ark::DynDataProfileExItem& item = match.profileEx.items[index];
        PushFilteredRow(result, Row({
            { L"Source", L"Profile EX Item" },
            { L"Index", std::to_wstring(index) },
            { L"FieldId", std::to_wstring(item.itemId) },
            { L"Kind", item.itemKind == KSW_DYN_PROFILE_EX_ITEM_KIND_GLOBAL_RVA ? L"GlobalRva" : L"StructOffset" },
            { L"Value", HexTextLocal(item.value) },
            { L"Flags", HexTextLocal(item.flags) },
            { L"Callback", (item.flags & KSW_DYN_PROFILE_EX_ITEM_FLAG_CALLBACK) != 0U ? L"是" : L"否" },
            { L"Profile", Utf8ToWideLocal(match.profileEx.profileName) },
        }), filterText);
    }
    if (match.profileEx.items.size() > itemLimit) {
        PushFilteredRow(result, Row({
            { L"Source", L"Profile EX Item" },
            { L"Status", L"Truncated" },
            { L"Shown", std::to_wstring(itemLimit) },
            { L"Total", std::to_wstring(match.profileEx.items.size()) },
        }), filterText);
    }
}

