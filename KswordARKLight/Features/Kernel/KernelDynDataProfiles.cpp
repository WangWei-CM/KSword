// BuildFieldDictionary converts fieldDictionary strings into protocol ids. Input
// is the pack root; processing keeps positional alignment with fields entries;
// output is an id vector, with 0 marking unknown dictionary names.
std::vector<std::uint32_t> BuildFieldDictionary(const JsonValue& root) {
    std::vector<std::uint32_t> dictionary;
    const JsonValue* fieldDictionary = root.member("fieldDictionary");
    if (fieldDictionary == nullptr || !fieldDictionary->isArray()) {
        return dictionary;
    }
    dictionary.reserve(fieldDictionary->arrayValue.size());
    for (const JsonValue& item : fieldDictionary->arrayValue) {
        dictionary.push_back(item.isString() ? FieldNameToId(item.stringValue) : 0U);
    }
    return dictionary;
}

// ParseProfileFields expands legacy fields [dictionaryIndex, offset] pairs into
// ArkDriverClient apply fields. Inputs are a profile object and root dictionary;
// processing rejects malformed pairs and unknown field ids; output is appended to
// the legacy apply packet and counted for diagnostics.
void ParseProfileFields(
    const JsonValue& profileObject,
    const std::vector<std::uint32_t>& dictionary,
    ksword::ark::DynDataProfileApplyInput& profileOut) {
    const JsonValue* fields = profileObject.member("fields");
    if (fields == nullptr || !fields->isArray()) {
        return;
    }

    for (const JsonValue& pairValue : fields->arrayValue) {
        if (!pairValue.isArray() || pairValue.arrayValue.size() < 2U) {
            continue;
        }
        std::uint32_t fieldIndex = 0;
        std::uint32_t offset = 0;
        if (!ParseUInt32(&pairValue.arrayValue[0], fieldIndex) ||
            !ParseUInt32(&pairValue.arrayValue[1], offset) ||
            fieldIndex >= dictionary.size()) {
            continue;
        }
        const std::uint32_t fieldId = dictionary[fieldIndex];
        if (fieldId == 0U || fieldId > KSW_DYN_FIELD_ID_MAX) {
            continue;
        }
        profileOut.fields.push_back({ fieldId, offset });
        if (profileOut.fields.size() >= KSW_DYN_PROFILE_MAX_FIELDS) {
            break;
        }
    }
}

// ItemKindToProtocol maps textual item kind names onto EX protocol constants.
// Input is a pack item kind; processing is case-insensitive; output is 0 when
// the item cannot be represented by the current driver protocol.
std::uint32_t ItemKindToProtocol(const std::string& kind) {
    const std::string lower = LowerAscii(kind);
    if (lower == "structoffset" || lower == "struct_offset" || lower == "offset") {
        return KSW_DYN_PROFILE_EX_ITEM_KIND_STRUCT_OFFSET;
    }
    if (lower == "globalrva" || lower == "global_rva" || lower == "rva") {
        return KSW_DYN_PROFILE_EX_ITEM_KIND_GLOBAL_RVA;
    }
    return 0U;
}

// ParseBoolFlag reads optional boolean-ish item flags. Input is one JSON value;
// processing accepts true/false, 0/1, and yes/no strings; output defaults false
// when absent or unrecognized to preserve legacy pack behavior.
bool ParseBoolFlag(const JsonValue* value) {
    if (value == nullptr) {
        return false;
    }
    if (value->type == JsonValue::Type::Bool) {
        return value->boolValue;
    }
    std::uint32_t numeric = 0;
    if (ParseUInt32(value, numeric)) {
        return numeric != 0U;
    }
    if (value->isString()) {
        const std::string lower = LowerAscii(value->stringValue);
        return lower == "true" || lower == "yes" || lower == "required" || lower == "callback";
    }
    return false;
}

// ParseExplicitFlags reads optional numeric/string flags from a typed item.
// Input is one item object; processing accepts `flags`, `required`, and callback
// source context; output contains only shared protocol-supported flag bits.
std::uint32_t ParseExplicitFlags(const JsonValue& itemObject, const bool callbackSource) {
    std::uint32_t flags = callbackSource ? KSW_DYN_PROFILE_EX_ITEM_FLAG_CALLBACK : 0U;
    std::uint32_t numericFlags = 0;
    if (ParseUInt32(itemObject.member("flags"), numericFlags)) {
        flags |= numericFlags;
    }
    if (ParseBoolFlag(itemObject.member("required"))) {
        flags |= KSW_DYN_PROFILE_EX_ITEM_FLAG_REQUIRED;
    }
    if (ParseBoolFlag(itemObject.member("callback"))) {
        flags |= KSW_DYN_PROFILE_EX_ITEM_FLAG_CALLBACK;
    }
    return flags & (KSW_DYN_PROFILE_EX_ITEM_FLAG_REQUIRED | KSW_DYN_PROFILE_EX_ITEM_FLAG_CALLBACK);
}

// ParseProfileItems expands `items`/`typedItems` and `callbackItems` into EX
// protocol items. Inputs are one matched profile object; processing maps item
// names to field ids and kind strings to protocol kinds; output is appended to
// profileExOut and truncated at the shared protocol limit.
void ParseProfileItems(const JsonValue& profileObject, ksword::ark::DynDataProfileApplyExInput& profileExOut) {
    auto parseArray = [&](const JsonValue* arrayValue, const bool callbackSource) {
        if (arrayValue == nullptr || !arrayValue->isArray()) {
            return;
        }
        for (const JsonValue& itemValue : arrayValue->arrayValue) {
            if (!itemValue.isObject()) {
                continue;
            }
            const std::string name = ParseString(itemValue, "name");
            const std::uint32_t itemId = FieldNameToId(name);
            const std::uint32_t itemKind = ItemKindToProtocol(ParseString(itemValue, "kind"));
            std::uint32_t value = 0;
            if (itemId == 0U || itemKind == 0U || !ParseUInt32(itemValue.member("value"), value)) {
                continue;
            }
            profileExOut.items.push_back({ itemId, itemKind, value, ParseExplicitFlags(itemValue, callbackSource) });
            if (profileExOut.items.size() >= KSW_DYN_PROFILE_EX_MAX_ITEMS) {
                return;
            }
        }
    };

    const JsonValue* items = profileObject.member("items");
    if (items == nullptr || !items->isArray() || items->arrayValue.empty()) {
        items = profileObject.member("typedItems");
    }
    parseArray(items, false);
    if (profileExOut.items.size() < KSW_DYN_PROFILE_EX_MAX_ITEMS) {
        parseArray(profileObject.member("callbackItems"), true);
    }
}

// ProfileArrayCount returns a compact count for diagnostics. Input is a JSON
// object and member key; processing checks array shape only; output is 0 when the
// member is missing or malformed.
std::uint32_t ProfileArrayCount(const JsonValue& profileObject, const std::string& key) {
    const JsonValue* value = profileObject.member(key);
    return value != nullptr && value->isArray() ? static_cast<std::uint32_t>(std::min<std::size_t>(value->arrayValue.size(), std::numeric_limits<std::uint32_t>::max())) : 0U;
}

// FillProfileMetadata copies identity and metadata into both apply packet shapes.
// Inputs are the current R0 identity and matched profile object; processing uses
// the same strings for legacy and EX APIs; output is ready for field/item append.
void FillProfileMetadata(
    const ksword::ark::ArkDynModuleIdentity& identity,
    const JsonValue& profileObject,
    ksword::ark::DynDataProfileApplyInput& profileOut,
    ksword::ark::DynDataProfileApplyExInput& profileExOut) {
    profileOut.ntoskrnl = identity;
    profileExOut.ntoskrnl = identity;
    profileOut.profileName = ParseString(profileObject, "profileName");
    profileOut.pdbName = ParseString(profileObject, "pdbName");
    profileOut.pdbGuid = ParseString(profileObject, "pdbGuid");
    ParseUInt32(profileObject.member("pdbAge"), profileOut.pdbAge);

    profileExOut.profileName = profileOut.profileName;
    profileExOut.pdbName = profileOut.pdbName;
    profileExOut.pdbGuid = profileOut.pdbGuid;
    profileExOut.pdbAge = profileOut.pdbAge;
}

// ProfileMatchesIdentity performs the exact kernel-image match required before
// applying PDB-derived offsets. Inputs are the current R0 identity and one JSON
// profile object; processing compares module class, machine, timestamp, and
// image size; output is true only for a safe exact match.
bool ProfileMatchesIdentity(const ksword::ark::ArkDynModuleIdentity& identity, const JsonValue& profileObject) {
    std::uint32_t moduleClassId = 0;
    std::uint32_t machine = 0;
    std::uint32_t timeDateStamp = 0;
    std::uint32_t sizeOfImage = 0;
    return ParseUInt32(profileObject.member("moduleClassId"), moduleClassId) &&
        ParseUInt32(profileObject.member("machine"), machine) &&
        ParseUInt32(profileObject.member("timeDateStamp"), timeDateStamp) &&
        ParseUInt32(profileObject.member("sizeOfImage"), sizeOfImage) &&
        identity.classId == moduleClassId &&
        identity.machine == machine &&
        identity.timeDateStamp == timeDateStamp &&
        identity.sizeOfImage == sizeOfImage;
}

// AppendDiagnostic records a bounded diagnostic line. Inputs are the output list
// and one message; processing prevents huge profile-scan summaries from flooding
// the UI; output is an updated diagnostics vector.
void AppendDiagnostic(std::vector<std::wstring>& diagnostics, std::wstring message) {
    constexpr std::size_t kMaxDiagnostics = 16;
    if (message.empty() || diagnostics.size() >= kMaxDiagnostics) {
        return;
    }
    diagnostics.push_back(std::move(message));
}

// BuildMatchedProfileResult fills the returned match after an identity hit.
// Inputs are the current identity, root dictionary, profile object, and path;
// processing parses both legacy fields and EX typed items; output is a complete
// match packet ready for display or ArkDriverClient apply.
DynDataProfileMatch BuildMatchedProfileResult(
    const ksword::ark::ArkDynModuleIdentity& identity,
    const JsonValue& rootObject,
    const JsonValue& profileObject,
    const std::filesystem::path& path,
    const std::uint32_t existingPackCount,
    const std::uint32_t profileCount,
    const std::uint32_t scannedProfileCount) {
    DynDataProfileMatch result;
    result.scanned = true;
    result.matched = true;
    result.path = path.wstring();
    result.existingPackCount = existingPackCount;
    result.profileCount = profileCount;
    result.scannedProfileCount = scannedProfileCount;
    result.fieldCount = ProfileArrayCount(profileObject, "fields");
    result.typedItemCount = ProfileArrayCount(profileObject, "items");
    if (result.typedItemCount == 0U) {
        result.typedItemCount = ProfileArrayCount(profileObject, "typedItems");
    }
    result.callbackItemCount = ProfileArrayCount(profileObject, "callbackItems");
    const JsonValue* coverage = profileObject.member("coveragePercent");
    result.coveragePercent = coverage != nullptr && coverage->isNumber() ? coverage->numberValue : -1.0;

    FillProfileMetadata(identity, profileObject, result.profile, result.profileEx);
    const std::vector<std::uint32_t> dictionary = BuildFieldDictionary(rootObject);
    ParseProfileFields(profileObject, dictionary, result.profile);
    ParseProfileItems(profileObject, result.profileEx);

    result.preferExApply = !result.profileEx.items.empty();
    result.valid = !result.profile.fields.empty() || !result.profileEx.items.empty();

    std::wostringstream message;
    if (result.valid) {
        message << L"本地 DynData profile pack 命中；legacyFields=" << result.profile.fields.size()
            << L"，typedItems=" << result.profileEx.items.size()
            << L"，packProfiles=" << result.profileCount
            << L"，scanned=" << result.scannedProfileCount
            << L"，applyMode=" << (result.preferExApply ? L"EX" : L"Legacy") << L"。";
    } else {
        message << L"本地 DynData profile pack 命中 identity，但可应用 fields/items 均为空或无法映射到当前协议。";
    }
    result.message = message.str();
    return result;
}

// ContainsI checks filter membership across display fields. Inputs are one text
// and optional filter; processing is case-insensitive using towlower; output is
// true when the filter is empty or contained by text.
bool ContainsI(std::wstring text, std::wstring filter) {
    if (filter.empty()) {
        return true;
    }
    std::transform(text.begin(), text.end(), text.begin(), [](const wchar_t ch) { return static_cast<wchar_t>(::towlower(ch)); });
    std::transform(filter.begin(), filter.end(), filter.begin(), [](const wchar_t ch) { return static_cast<wchar_t>(::towlower(ch)); });
    return text.find(filter) != std::wstring::npos;
}

// PushFilteredRow appends a display row only when it matches the current UI
// filter. Inputs are the target result, row, and filter; processing scans all
// columns and detail text; no value is returned.
void PushFilteredRow(KernelOperationResult& result, KernelResultRow row, const std::wstring& filter) {
    std::wstring haystack = row.detailText;
    for (const auto& [key, value] : row.columns) {
        haystack += L" ";
        haystack += key;
        haystack += L"=";
        haystack += value;
    }
    if (ContainsI(haystack, filter)) {
        result.rows.push_back(std::move(row));
    }
}

// ApplyModeText returns the driver API selected by parsed profile content. Input
